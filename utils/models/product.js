export class Product{
    constructor(id, name, description, price, imageUrl, owner, ownerActive, buyer, ownerName){
        this.id = id, 
        this.name = name,
        this.description = description, 
        this.price = price, 
        this.imageUrl = imageUrl,
        this.owner = owner, 
        this.ownerActive = ownerActive, 
        this.buyer = buyer, 
        this.ownerName = ownerName
    }
}